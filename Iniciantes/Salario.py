# Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês.
# Calcule e mostre o total do seu salário no referido mês.

ganho_hora = float(input("Entre com o valor ganhado por hora: "))
horas_mes = float(input("Entre com o numero de horas trabalhadas no mês: "))
salario = (ganho_hora * horas_mes)
print("O soma do salario referido a este mês é de: ", salario)
